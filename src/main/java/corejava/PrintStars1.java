package corejava;

public class PrintStars1 {
	public static void main(String[] args){
		
		for(int i=1;i<=5;i++){
			
			
			for(int j=i;j<=5;j++){
					
				//System.out.print(" ");
				System.out.print("*");//*,*,*,*,*
				//System.out.print(" ");
			}
			
			System.out.println();
			for(int k=i;k>0;k--){
				System.out.print(" ");
			}
		}
	}
	}

